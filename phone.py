class Phone:
        """

        Holds data about phones

        """

        def __init__(self, name, rank, profitability, cpu, ram, hdr, display, os, camera, aku, price):
            self.name = name
            self.rank = rank
            self.profitability = profitability
            self.cpu = cpu
            self.ram = ram
            self.hdr = hdr
            self.display = display
            self.os = os
            self.camera = camera
            self.aku = aku
            self.price = price








